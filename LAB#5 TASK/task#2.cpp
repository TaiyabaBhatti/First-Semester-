/*Task # 2: Generate a series of Odd numbers and even numbers (up to 20) using for 
 loop*/

#include<iostream>
using namespace std;
int main()
{
	
	cout<<"odd"<<'\t'<<"even"<<endl;
	
	for(int x=1, y=2;x<=10,y<=10;x+=2,y+=2)
		cout<<x<<'\t'<<y<<endl;
		

	
	
	
	
	
	
	
	
	
	
	
    return 0;
	
}
